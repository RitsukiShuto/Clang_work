#include<stdio.h>
int main(){
    /* 算術演算 */
    printf("1 + 2 = %d\n", 1 + 2);      // 加算
    printf("1 - 2 = %d\n", 1 - 2);      // 減算
    printf("1 * 2 = %d\n", 1 * 2);      // 乗算
    printf("1 / 2 = %d\n", 1 / 2);      // 除算
    printf("1 %% 2 = %d\n", 1 % 2);     // 剰余算

    return 0;
}